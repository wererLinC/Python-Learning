# sort

cars = ['bmw', 'audi', 'toyota', 'qiaoke']
print(cars)
cars.sort()
print(cars)

# reverse = True

cars = ['bmw', 'audi', 'toyota', 'qiaoke']
print(cars)
cars.sort(reverse = True)
print(cars)

# sorted

cars = ['bmw', 'audi', 'toyota', 'qiaoke']
print(cars)
print(sorted(cars))

# reverse

cars = ['bmw', 'audi', 'toyota', 'qiaoke']
print(cars)
cars.reverse()
print(cars)

# len

lens = len(cars)
print(lens)
